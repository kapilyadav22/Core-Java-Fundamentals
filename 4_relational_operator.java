class relational_operator {
    public static void main(String[] args) {
        //relational statements
        int num1 = 8;
        int num2 = 9;

        boolean result = num1<num2;
        System.out.println(result);
        result = num1==num2;
        System.out.println(result);
        result = num1>num2;
        System.out.println(result);
        result = num1<=num2;
        System.out.println(result);
        result = num1>=num2;
        System.out.println(result);
    }
}
