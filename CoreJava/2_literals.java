class literals {
    public static void main(String args[]){
        //literals
        int num1 = 0b0101; //bput 0b for binary number
        int num2 = 1_00_00_00; //print the zeroes using underscore
        int num3 = 0x1234 ; // put 0x for hexadecimal;
        int num4 = 012334; //put 0 in beginning for octal;
        char ch = 'a';
        ch++;
        System.out.println(ch);

        String s = "Hey, My name is Kapil.";
        System.out.println(s);

        boolean sign = true;
        System.out.println(sign);
        
        int a = 'a';
        System.out.println("Value of a is : "+ a);

        char asci = 97;
        System.out.println("ASCII Value of 97 is : "+ asci);

        
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
    
    }
}
